const x = 5;

// operator ==
console.log(x == 8);
console.log(x == 5);
console.log(x == "5");

// operator ===
console.log(x === 8);
console.log(x === 5);
console.log(x === "5");

// operator !=
console.log(x != 8);

// operator !==
console.log(x !== 5);
console.log(x !== "5");
console.log(x !== "8");

// operator >
console.log(x > 8);

// operator <
console.log(x < 8);

// operator >=
console.log(x >= 8);

// operator <=
console.log(x <= 8);
